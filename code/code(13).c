/*句子首字母小写转大写*/ 
#include <stdio.h>
void main()
{
	char s[1000] = {0};//初始化1000个字符型数组 
	int i = 0;//定义i循环体为 0
	printf("请输入一句英文句子：");
	gets(s);//获取你要输入的句子 
	for(i = 0;s[i] != '\0';i++)//判断是不是句末 
	{
		if(s[i] == ' ' && s[i+1] >= 'a' && s[i+1] <= 'z')//转换的条件，如果遇到空格，则将首字母转换为大写 
		{
			s[i+1] -= 32;
		}
		if(s[0] >= 'a'&& s[0] <= 'z') //转换第一个字母为大写 
		{
			s[0] -= 32;
		}
	}
	puts(s);
 } 

